async function main() {
  let created = 0;
  let skipped = 0;

  for (const p of PRODUCTS) {
    const existing = await prisma.product.findUnique({
      where: { slug: p.slug },
    });

    if (existing) {
      skipped++;
      continue;
    }

    await prisma.product.create({
      data: {
        slug: p.slug,
        name: p.name,
        category: p.category,
        description: p.description,
        type: p.type || "other",
        basePrice: p.basePrice,
        pricingUnit: p.pricingUnit || "per_piece",
        isActive: true,
        sortOrder: p.sortOrder || 0,
        acceptedFormats: p.acceptedFormats || PRINT_FORMATS,
        minDpi: p.minDpi ?? 150,
        requiresBleed: p.requiresBleed ?? true,
        bleedIn: p.bleedIn ?? 0.125,
      },
    });
    created++;
  }

  console.log(`Done. Created: ${created}, Skipped (already exist): ${skipped}`);

  // Print counts per category
  const cats = await prisma.product.groupBy({
    by: ["category"],
    where: { isActive: true },
    _count: true,
    orderBy: { _count: { category: "desc" } },
  });
  console.log("\nProduct counts by category:");
  for (const c of cats) {
    console.log(`  ${c.category}: ${c._count} products`);
  }
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
